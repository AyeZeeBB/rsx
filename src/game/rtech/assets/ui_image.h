static constexpr const uint32_t BC1_TILE_SIZE = 512u;
static constexpr const uint32_t BC1_FLAG = 0x40u;

static constexpr const uint32_t BC7_TILE_SIZE = BC1_TILE_SIZE * 2u;
static constexpr const uint32_t BC7_FLAG = 0x41u;

struct UIImageAssetFlags_t
{
	uint8_t hasLowTableBc1 : 1;
	uint8_t hasLowTableBc7 : 1;
	uint8_t hasHighTableBc1 : 1;
	uint8_t hasHighTableBc7 : 1;
	// 0x1 = LZ
	// 0x2 = Snowflake
	uint8_t compressionType : 2;
	uint8_t sizeShift : 2;

	uint8_t unkBits;
};
static_assert(sizeof(UIImageAssetFlags_t) == sizeof(uint16_t));

enum eUIImageTileType : uint8_t
{
	TYPE_LQ,
	TYPE_HQ,
	TYPE_COUNT
};

enum eUIImageResType : uint8_t
{
	UNK_RES,
	LQ_RES,
	HQ_RES,
	LQ_AND_HQ_RES
};

struct UIImageTile_t
{
	uint32_t offset : 24;
	uint32_t opcode : 8;
};
static_assert(sizeof(UIImageTile_t) == sizeof(uint32_t));

struct UIImageAssetData_v2_t
{
	// No clue what this float data is, UVS?
	float unk_00;
	float unk_04;
	float unk_08;
	float unk_0C;
	float unk_10;
	float unk_14;
	float unk_18;
	float unk_1C;

	uint16_t highResWidth;
	uint16_t highResHeight;
	uint16_t lowResWidth;
	uint16_t lowResHeight;

	void* data;
	char* name;

	char unk_38[8]; // Always zero, probably padding.
};
static_assert(sizeof(UIImageAssetData_v2_t) == 0x40);

// Struct should match proper dataTypes, based on reverse engineering, besides the float table.
struct UIImageAssetHeader_v2_t
{
	uint32_t unk_00;
	uint32_t unk_04;
	int32_t unk_08; // Always negative.

	uint32_t streamedOffset; // hdr->streamedOffset = *a4 >> 12; in LoadUIIAsset, seems to be starpak offset. Packed just like the compressed texture sizes most likely.
	uint16_t streamedSize; // packed starpak size?

	UIImageAssetFlags_t imgFlags;

	uint8_t unk_14;
	uint8_t unk_15;
	uint16_t unk_16;

	uint16_t width;
	uint16_t height;

	uint32_t unk_1C;

	float unknownFloats[8]; // UVS?
};
static_assert(sizeof(UIImageAssetHeader_v2_t) == 0x40);

class UIImageAsset
{
public:
	UIImageAsset(const UIImageAssetHeader_v2_t* const hdr, const UIImageAssetData_v2_t* const data) : width(hdr->width), height(hdr->height), imgFlags(hdr->imgFlags), streamedOffset(hdr->streamedOffset), streamedSize(hdr->streamedSize),
	name(data->name), data(data->data),
	resData(), shouldStream(false), compType(static_cast<eCompressionType>(hdr->imgFlags.compressionType))
	{
		resData[eUIImageTileType::TYPE_LQ].width  = data->lowResWidth;
		resData[eUIImageTileType::TYPE_LQ].height = data->lowResHeight;

		resData[eUIImageTileType::TYPE_HQ].width  = data->highResWidth;
		resData[eUIImageTileType::TYPE_HQ].height = data->highResHeight;
	};

	uint16_t width;
	uint16_t height;

	UIImageAssetFlags_t imgFlags;

	uint64_t streamedOffset;
	uint64_t streamedSize;

	char* name;
	void* data;

	//
	struct QualityData
	{
		uint32_t totalTiles;
		std::unique_ptr<UIImageTile_t[]> tilePoints;

		uint16_t width;
		uint16_t height;

		uint32_t widthBlocks;
		uint32_t heightBlocks;

		uint16_t shiftedWidth;
		uint16_t shiftedHeight;

		uint32_t numBc1Tiles;
		uint32_t numBc7Tiles;

		bool copiedTable;
	} resData[eUIImageTileType::TYPE_COUNT];

	bool shouldStream;
	eCompressionType compType;
};

